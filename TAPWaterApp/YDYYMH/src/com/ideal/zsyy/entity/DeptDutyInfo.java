package com.ideal.zsyy.entity;

public class DeptDutyInfo {

	private String id;
	private String dept_id;
	private String dept_name;
	private String dept_infosName;
	private String locate;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDept_id() {
		return dept_id;
	}
	public void setDept_id(String deptId) {
		dept_id = deptId;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String deptName) {
		dept_name = deptName;
	}
	public String getDept_infosName() {
		return dept_infosName;
	}
	public void setDept_infosName(String deptInfosName) {
		dept_infosName = deptInfosName;
	}
	public String getLocate() {
		return locate;
	}
	public void setLocate(String locate) {
		this.locate = locate;
	}
	
	
}
