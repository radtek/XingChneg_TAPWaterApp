package com.ideal.zsyy.entity;

public class MZChargeInfo {

	private String Id;
	private String itemName;
	private String itemGG;
	private String itemNum;
	private String itemPrice;
	private String itemTotle;
	private String itemTime;
	private String unit;
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemGG() {
		return itemGG;
	}
	public void setItemGG(String itemGG) {
		this.itemGG = itemGG;
	}
	public String getItemNum() {
		return itemNum;
	}
	public void setItemNum(String itemNum) {
		this.itemNum = itemNum;
	}
	public String getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}
	public String getItemTotle() {
		return itemTotle;
	}
	public void setItemTotle(String itemTotle) {
		this.itemTotle = itemTotle;
	}
	public String getItemTime() {
		return itemTime;
	}
	public void setItemTime(String itemTime) {
		this.itemTime = itemTime;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	
}
